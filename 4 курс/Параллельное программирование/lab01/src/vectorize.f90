! Векторизация — это процесс преобразования алгоритмов или программ так, чтобы они могли эффективно использовать
! векторные (SIMD) инструкции процессора. Это позволяет выполнять одну и ту же операцию сразу над несколькими 
! элементами данных за одну инструкцию. Векторизация особенно эффективна в задачах, где однотипные операции (например, 
! сложение, умножение) выполняются над большими массивами данных.

! Пример:

! Без векторизации (скалярный код): процессор обрабатывает каждое число по очереди.
! С векторизацией: процессор обрабатывает несколько чисел одновременно, используя SIMD регистры.
! Это ускоряет выполнение программ, которые оперируют большими массивами данных, такими как матричные вычисления, 
! обработка изображений, задачи машинного обучения и т.д.

! ---------------------------------------

! SIMD (Single Instruction, Multiple Data) — это набор инструкций, которые позволяют процессору выполнять одну и ту же 
! операцию одновременно на нескольких данных. SIMD инструкции применяются к векторным регистрам, в которых хранится 
! сразу несколько элементов данных. То есть, за одну тактовую частоту процессор выполняет одну инструкцию, но на 
! нескольких элементах данных.

! Пример:

! SIMD инструкция может сложить 4 пары чисел за один такт, тогда как обычная (скалярная) инструкция сложила бы только 
! одну пару чисел за один такт.